def get_collisions(map_name):
    """
    Retorna la lista de rectángulos de colisión para el nombre del mapa dado.
    Si el nombre del mapa contiene '_noche', se puede aplicar lógica especial,
    por ejemplo, usando la misma configuración que la versión diurna.
    """
    if "_noche" in map_name:
        # Se asume que la versión nocturna usa los mismos obstáculos que la versión diurna.
        daytime_key = map_name.replace("_noche", "")
        return collision_data.get(daytime_key, [])
    return collision_data.get(map_name, [])
